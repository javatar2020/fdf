#include "libft.h"

int		ft_atoi(const char *str)
{
	long	res;
	int		sign;

	res = 0;
	sign = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
		sign = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (ft_isdigit(*str))
		res = res * 10 + *str++ - 48;
	if (res < 0 && sign == 1)
		return (-1);
	if (res < 0 && sign == -1)
		return (0);
	return (res * sign);
}
